def is_docker_installed():
    """Verifica si Docker está instalado en el sistema."""
    try:
        subprocess.run(["docker", "--version"], check=True, stdout=subprocess.PIPE, stderr=subprocess.PIPE)
        print("Docker está instalado.")
        return True
    except subprocess.CalledProcessError:
        print("Docker no está instalado.")
        return False

# Función para detectar la distribución de Linux leyendo /etc/os-release
def detect_linux_distro():
    distro_id = "unknown"
    try:
        with open("/etc/os-release") as f:
            for line in f:
                if line.startswith("ID="):
                    distro_id = line.strip().split("=")[1].strip('"').lower()
                    break
    except Exception as e:
        print(f"No se pudo detectar la distribución Linux: {e}")
    return distro_id

# Función para detectar el sistema operativo
def detect_os():
    """Detecta el sistema operativo en el que se ejecuta el script."""
    os_name = platform.system().lower()
    if os_name == "windows":
        return "windows"
    elif os_name == "darwin":
        return "macos"
    elif os_name == "linux":
        # Detecta la distribución específica de Linux
        distro = detect_linux_distro()
        if distro in ["ubuntu", "debian"]:
            return distro
        elif distro in ["centos", "fedora", "arch"]:
            return distro
        else:
            # Si no se reconoce la distro, se asume como "linux"
            return "linux"
    else:
        raise Exception("Sistema operativo no soportado.")

# Función para instalar Docker en Windows
def install_docker_windows():
    """Instala Docker en Windows."""
