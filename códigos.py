# Función para instalar Docker en Arch Linux
def install_docker_arch():
    """Instala Docker en Arch Linux."""
    print("Instalando Docker en Arch Linux...")
    try:
        subprocess.run(["sudo", "pacman", "-Syu", "--noconfirm", "docker"], check=True)
        subprocess.run(["sudo", "systemctl", "start", "docker"], check=True)
        subprocess.run(["sudo", "systemctl", "enable", "docker"], check=True)
        print("Docker instalado correctamente en Arch Linux.")
    except subprocess.CalledProcessError:
        print("Error al intentar instalar Docker en Arch Linux.")
        sys.exit(1)

# Función para instalar Docker en Linux, delegando según la distribución detectada
def install_docker_linux(distro):
    if distro in ["ubuntu", "debian"]:
        install_docker_debian()
    elif distro == "centos":
        install_docker_centos()
    elif distro == "fedora":
        install_docker_fedora()
    elif distro == "arch":
        install_docker_arch()
    else:
        print("Distribución Linux no soportada para la instalación automática de Docker.")
        sys.exit(1)

# Función para instalar Docker Compose según el sistema operativo
def install_docker_compose(os_type):
    if os_type in ["ubuntu", "debian", "centos", "fedora", "arch", "linux"]:
        print("Docker Compose no está instalado. Instalando...")
        # Se asume que en sistemas Linux se usa el gestor de paquetes correspondiente o se instala vía pip
        if os_type in ["ubuntu", "debian"]:
            try:
                subprocess.run(["sudo", "apt-get", "install", "-y", "docker-compose"], check=True)
                print("Docker Compose instalado correctamente.")
            except subprocess.CalledProcessError:
                print("Error al intentar instalar Docker Compose en Debian/Ubuntu.")
                sys.exit(1)
        elif os_type in ["centos", "fedora"]:
            try:
                subprocess.run(["sudo", "yum", "install", "-y", "docker-compose"], check=True)
                print("Docker Compose instalado correctamente.")
            except subprocess.CalledProcessError:
                print("Error al intentar instalar Docker Compose en CentOS/Fedora.")
                sys.exit(1)
        elif os_type == "arch":
            try:
                subprocess.run(["sudo", "pacman", "-Syu", "--noconfirm", "docker-compose"], check=True)
                print("Docker Compose instalado correctamente.")
            except subprocess.CalledProcessError:
                print("Error al intentar instalar Docker Compose en Arch Linux.")
                sys.exit(1)
        else:
            # Como alternativa, se puede instalar vía pip
            try:
                subprocess.run(["sudo", "pip", "install", "docker-compose"], check=True)
                print("Docker Compose instalado correctamente vía pip.")
            except subprocess.CalledProcessError:
                print("Error al intentar instalar Docker Compose vía pip.")
                sys.exit(1)
    else:
        print("Instalación de Docker Compose no soportada para este sistema operativo.")
        sys.exit(1)

